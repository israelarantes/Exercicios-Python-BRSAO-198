# Neste programa realizamos uma soma simples, de duas variáveis.
# As variáveis são inseridas pelo usuário através da função input.
# É realizada a operação e soma e atribuido o resultado a uma variável e ela é impressa na tela.

a = int(input("Insira o número: "))
b = int(input("Insira outro número: "))

a = (a+b)

print ("A soma destes dois números é: ", a) 
